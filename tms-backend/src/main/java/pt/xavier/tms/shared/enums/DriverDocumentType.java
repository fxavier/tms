package pt.xavier.tms.shared.enums;

/**
 * Types of documents that can be associated with a driver (Requisito 6.4).
 */
public enum DriverDocumentType {

    /** Driving licence (carta de condução). */
    CARTA_CONDUCAO,

    /** National identity card (bilhete de identidade). */
    BILHETE_IDENTIDADE,

    /** Any other operational document. */
    OUTRO
}
